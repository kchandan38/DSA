package com.datastructure.bitwiseoperations;

// Not (~) Operation example
public class Lab5 {

    public static void main(String[] args) {

        int a = 2; //0010 -> 0011
        int b = 4;
        System.out.println(~a);
        System.out.println(~~a);
        System.out.println(~b);
        System.out.println(~~b);


    }
}
